void solve()
{
    i64 n, C;
    cin >> n >> C;
    vector<i64> hd2c(C + 1);
    vector<i64> HD2c(C + 1);
    for (int i = 0; i < n; i++) {
        i64 ci, di, hi;
        cin >> ci >> di >> hi;
        hd2c[ci] = max(hd2c[ci], di * hi);
    }
    for (int i = 1; i <= C; i++) {
        if (hd2c[i] != 0) {
            for (int j = 1; j * i <= C; j++) {
                HD2c[j * i] = max(hd2c[i] * j, HD2c[j * i]);
            }
        }
    }
    for (int i = 1; i <= C; i++) {
        HD2c[i] = max(HD2c[i], HD2c[i - 1]);
    }
    i64 m;
    cin >> m;
    while (m--) {
        i64 Di, Hi;
        cin >> Di >> Hi;
        i64  DH = Di * Hi;
        auto it = upper_bound(HD2c.begin() + 1, HD2c.end(), DH);
        if (it == HD2c.end()) {
            cout << -1 << '\n';
        } else {
            cout << it - HD2c.begin() << '\n';
        }
    }
}

