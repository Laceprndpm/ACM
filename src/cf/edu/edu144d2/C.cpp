struct Comb {
    int       n;
    vector<Z> _fac;
    vector<Z> _invfac;
    vector<Z> _inv;

    Comb() : n{0}, _fac{1}, _invfac{1}, _inv{0} {}

    Comb(int n) : Comb()
    {
        init(n);
    }

    void init(int m)
    {
        if (m <= n) return;
        _fac.resize(m + 1);
        _invfac.resize(m + 1);
        _inv.resize(m + 1);

        for (int i = n + 1; i <= m; i++) {
            _fac[i] = _fac[i - 1] * i;
        }
        _invfac[m] = _fac[m].inv();
        for (int i = m; i > n; i--) {
            _invfac[i - 1] = _invfac[i] * i;
            _inv[i]        = _invfac[i] * _fac[i - 1];
        }
        n = m;
    }

    Z fac(int m)
    {
        if (m > n) init(2 * m);
        return _fac[m];
    }

    Z invfac(int m)
    {
        if (m > n) init(2 * m);
        return _invfac[m];
    }

    Z inv(int m)
    {
        if (m > n) init(2 * m);
        return _inv[m];
    }

    Z binom(int n, int m)
    {
        if (n < m || m < 0) return 0;
        return fac(n) * invfac(m) * invfac(n - m);
    }
} comb(1e6 + 50);

int workcnt(int l, int r)
{
    int cnt = 0;
    while (l <= r) {
        cnt++;
        l *= 2;
    }
    return cnt;
}

i64 qpow(i64 n, i64 q, i64 mod = MOD)
{
    if (q == 0) {
        return 1;
    }
    i64 ans = 1;
    while (q > 0) {
        if (q & 1) {
            ans *= n;
            ans %= mod;
        }
        n *= n;
        n %= mod;
        q >>= 1;
    }
    return ans;
}

void solve()
{
    i64 l, r;
    cin >> l >> r;
    int ans1 = workcnt(l, r);
    cout << ans1 << ' ';
    // 怀疑需要分块
    // 4 8  16 32 64
    // 4 8  16 32 96
    // 4 8  16 48 96
    // 4 8  24 48 96
    // 4 12 24 24 96
    // 5 10 20 40 80
    // 6 12 24 48 96
    //
    Z ans2 = 0;
    for (int j = ans1 - 1; j >= 0; j--) {
        i64 times = qpow(2, j);
        times *= qpow(3, ans1 - 1 - j);
        // j个2，共ans1 - 1个位置...Cn j
        i64 bound = r / times;
        if (bound < l) {
            break;
        }
        ans2 += comb.binom(ans1 - 1, j) * (bound - l + 1);
    }
    cout << ans2;
    cout << '\n';
}

