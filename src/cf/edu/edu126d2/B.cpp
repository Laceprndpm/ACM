#include <iostream>
using namespace std;
using i64         = long long;
using PII         = pair<int, int>;
constexpr int INF = 1e9;

int check(int x)
{
    int cnt = 0;
    x %= 32768;
    while (x) {
        cnt++;
        x <<= 1;
        x %= 32768;
    }
    return cnt;
}

void solve()
{
    int n;
    cin >> n;
    // 0b 10000000 00000000
    int ans = 20;
    for (int i = 0; i <= 16; i++) {
        ans = min(i + check(i + n), ans);
    }
    cout << ans << '\n';
}

