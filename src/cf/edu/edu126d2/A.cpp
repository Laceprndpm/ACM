#include <utility>
#include <vector>
#define REMOVE_ME
#include <iostream>
using namespace std;
using i64         = long long;
using PII         = pair<int, int>;
constexpr int INF = 1e9;

void solve()
{
    int n;
    cin >> n;
    vector<int> ai(n + 1);
    vector<int> bi(n + 1);
    for (int i = 1; i <= n; i++) {
        cin >> ai[i];
    }
    for (int i = 1; i <= n; i++) {
        cin >> bi[i];
    }
    for (int i = 1; i <= n; i++) {
        if (ai[i] < bi[i]) {
            swap(ai[i], bi[i]);
        }
    }
    i64 ans = 0;
    for (int i = 1; i <= n - 1; i++) {
        ans += abs(ai[i + 1] - ai[i]) + abs(bi[i + 1] - bi[i]);
    }
    cout << ans << '\n';
}

