struct Comb {
    int            n;
    std::vector<Z> _fac;
    std::vector<Z> _invfac;
    std::vector<Z> _inv;

    Comb() : n{0}, _fac{1}, _invfac{1}, _inv{0} {}

    Comb(int n) : Comb()
    {
        init(n);
    }

    void init(int m)
    {
        if (m <= n) return;
        _fac.resize(m + 1);
        _invfac.resize(m + 1);
        _inv.resize(m + 1);

        for (int i = n + 1; i <= m; i++) {
            _fac[i] = _fac[i - 1] * i;
        }
        _invfac[m] = _fac[m].inv();
        for (int i = m; i > n; i--) {
            _invfac[i - 1] = _invfac[i] * i;
            _inv[i]        = _invfac[i] * _fac[i - 1];
        }
        n = m;
    }

    Z fac(int m)
    {
        if (m > n) init(2 * m);
        return _fac[m];
    }

    Z invfac(int m)
    {
        if (m > n) init(2 * m);
        return _invfac[m];
    }

    Z inv(int m)
    {
        if (m > n) init(2 * m);
        return _inv[m];
    }

    Z binom(int n, int m)
    {
        if (n < m || m < 0) return 0;
        return fac(n) * invfac(m) * invfac(n - m);
    }
} comb;

void zeta(vector<Z> &f)
{
    int n = __builtin_ctz(f.size());
    for (int i = 0; i < n; ++i)
        for (int mask = 0; mask < (1 << n); ++mask)
            if (!(mask & (1 << i))) f[mask | (1 << i)] += f[mask];
}

void mobius(vector<Z> &f)
{
    int n = __builtin_ctz(f.size());
    for (int i = 0; i < n; ++i)
        for (int mask = 0; mask < (1 << n); ++mask)
            if (!(mask & (1 << i))) f[mask | (1 << i)] -= f[mask];
}

void Or(vector<Z> &f, ll type)  // 迭代实现，常数更小
{
    int n = sz(f);
    for (ll x = 2; x <= n; x <<= 1) {
        ll k = x >> 1;
        for (ll i = 0; i < n; i += x) {
            for (ll j = 0; j < k; j++) {
                f[i + j + k] += f[i + j] * type;
            }
        }
    }
}

vector<Z> calc(const vector<Z> &a, const vector<Z> &b)
{
    int       n  = a.size();
    vector<Z> fa = a, fb = b;
    Or(fa, 1);
    Or(fb, 1);
    for (int i = 0; i < n; ++i) fa[i] *= fb[i];
    Or(fa, Z::mod() - 1);
    return fa;
}

void solve()
{
    int n, m, k;
    cin >> n >> m >> k;
    vector<pair<int, int>> edge(n);

    vector<vector<int>> graph(n + 1);
    for (int i = 1; i <= n - 1; i++) {
        int u, v;
        cin >> u >> v;
        graph[u].eb(v);
        graph[v].eb(u);
        edge[i] = {u, v};
    }
    vector<int> tree_route(n + 1);
    vector<int> depth(n + 1);

    auto dfs = [&](auto &&dfs, int u, int fa) -> void {
        for (int v : graph[u]) {
            if (v == fa) continue;
            depth[v] = depth[u] + 1;
            dfs(dfs, v, u);
        }
    };

    depth[1] = 0;
    dfs(dfs, 1, 1);

    for (int i = 0; i < m; i++) {
        int which;
        cin >> which;
        auto [u, v] = edge[which];
        if (depth[u] < depth[v]) swap(u, v);
        tree_route[u] |= 1 << i;
    }
    auto dfs2 = [&](auto &&dfs2, int u, int fa) -> void {
        for (int v : graph[u]) {
            if (v == fa) continue;
            tree_route[v] |= tree_route[u];
            dfs2(dfs2, v, u);
        }
    };
    dfs2(dfs2, 1, 1);

    vector<pair<int, int>> vec(k);
    for (int i = 0; i < k; i++) {
        int s, t;
        cin >> s >> t;
        vec[i] = {s, t};
    }

    vector<Z> dp1(1 << (m));

    for (int i = 0; i < k; i++) {
        int res = tree_route[vec[i].fi] ^ tree_route[vec[i].se];
        dbg(res);
        dp1[res] += 1;
    }
    const int mask = (1 << m) - 1;
    auto      cur  = dp1;
    for (int i = 1; i < 22; i++) {
        Z ans = cur[(1 << m) - 1];
        if (ans != 0) {
            cout << i << ' ' << ans / comb.fac(i) << '\n';
            return;
        }
        dbg(cur);
        cur = calc(cur, dp1);
        dbg(cur);
    }
    cout << -1 << '\n';
}

