struct Prime {
    vector<int> minp, primes;

    Prime(int n)
    {
        sieve(n);
    }

    void sieve(int n)
    {
        minp.assign(n + 1, 0);
        primes.clear();

        for (int i = 2; i <= n; i++) {
            if (minp[i] == 0) {
                minp[i] = i;
                primes.push_back(i);
            }

            for (auto p : primes) {
                if (i * p > n) {
                    break;
                }
                minp[i * p] = p;
                if (p == minp[i]) {
                    break;
                }
            }
        }
    }
} prime(2240);

void solve()
{
    int n;
    cin >> n;
    vector<int> arr(n);
    for (int i = 0; i < n; i++) {
        cin >> arr[i];
    }
    if (n % 2) {
        cout << "YES\n";
        return;
    }
    if (n == 2) {
        if (arr[0] != arr[1]) {
            cout << "NO\n";
            return;
        } else {
            cout << "YES\n";
            return;
        }
    }
    bool ok = 1;
    for (int p : prime.primes) {
        int cnt = 0;
        for (int i = 0; i < n; i++) {
            while (arr[i] % p == 0) {
                arr[i] /= p;
                cnt++;
            }
        }
        if (cnt % 2) {
            ok = 0;
        }
    }
    map<int, int> remain;
    for (int i : arr) {
        if (i != 1) {
            remain[i]++;
        }
    }
    for (auto [_, i] : remain) {
        if (i % 2) {
            ok = 0;
        }
    }
    if (ok) {
        cout << "YES\n";
    } else {
        cout << "NO\n";
    }
}

